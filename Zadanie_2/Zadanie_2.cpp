#include <iostream>

int main()
{
    int a = 150;
    float b = 15.933f;
    short c = 250;
    std::cout << "a =" << a << "\n";
    std::cout << "b =" << b << "\n";
    std::cout << "c =" << c << "\n";
    std::string day = "02";
    int month = 12;
    int year = 2003;
    std::cout << day << "." << month << "." << year << "\n";
    const float d = 2.3f;
    const std::string e = "WINDOWS";
    std::cout << d << " " << e;

}

